# main.py

def ask_for_age():
    """
    Задание 1:
    Запросите у пользователя возраст с помощью input().
    Если возраст >= 18, выведите "Вы совершеннолетний".
    Иначе — "Вы несовершеннолетний".
    """
    age = int(input("Введите возраст: "))
    if age >= 18:
        print("Вы совершеннолетний")
    else:
        print("Вы несовершеннолетний")


def check_password():
    """
    Задание 2:
    Запросите у пользователя пароль.
    Если введенный пароль равен "secret", выведите "Пароль верный".
    Иначе — "Пароль неверный".
    """
    password = input("Введите пароль: ")
    if password == "secret":
        print("Пароль верный")
    else:
        print("Пароль неверный")


def check_temperature():
    """
    Задание 3:
    Запросите у пользователя температуру в градусах.
    - Если температура < 15, выведите "Холодно".
    - Если 15 <= температура <= 25, выведите "Тепло".
    - Если температура > 25, выведите "Жарко".
    """
    temp = float(input("Введите температуру: "))
    if temp < 15:
        print("Холодно")
    elif 15 <= temp <= 25:
        print("Тепло")
    else:
        print("Жарко")


def check_login_and_password():
    """
    Задание 4:
    Запросите логин и пароль.
    Если логин "admin" И пароль "password123", выведите "Доступ разрешен".
    Иначе — "Доступ запрещен".
    """
    login = input("Введите логин: ")
    password = input("Введите пароль: ")
    if login == "admin" and password == "password123":
        print("Доступ разрешен")
    else:
        print("Доступ запрещен")


# Этот блок будет выполняться только при запуске файла напрямую,
# а не при импорте (например, в тестах pytest)
if __name__ == "__main__":
    ask_for_age()
    check_password()
    check_temperature()
    check_login_and_password()
